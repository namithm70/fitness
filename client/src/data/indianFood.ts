export const getAllIndianFoods = (): Food[] => {
  return indianFoodCategories.flatMap(category => category.foods);
};

export const getIndianFoodsByCategory = (categoryId: string): Food[] => {
  const category = indianFoodCategories.find(cat => cat.id === categoryId);
  return category ? category.foods : [];
};

export const searchIndianFoods = (query: string): Food[] => {
  const allFoods = getAllIndianFoods();
  return allFoods.filter(food => 
    food.name.toLowerCase().includes(query.toLowerCase()) ||
    food.dietaryTags?.some(tag => tag.toLowerCase().includes(query.toLowerCase()))
  );
};
